//===============================================
#include <iostream>
//===============================================
using namespace std;
//===============================================
void loop1(const int& n) {
    int i = 0;
    while(i < n) {
        cout << "While : " << i << "\n";
        i++;
    }
}
//===============================================
void loop2(const int& n) {
    for(int i = 0; i < n; i++) {
        cout << "For : " << i << "\n";
    }
}
//===============================================
void loop3(const int& n) {
    int i = 0;
    do {
        cout << "Do While : " << i << "\n";
        i++;
    } while(i < n);
}
//===============================================
int main(int argc, char** argv) {
    cout << "-------------------------------------------------\n";
    loop1(10);
    cout << "-------------------------------------------------\n";
    loop2(10);
    cout << "-------------------------------------------------\n";
    loop3(10);
    cout << "-------------------------------------------------\n";
   return 0;
}
